<?php 
    $a = 10;
    $b = 2;

    // Soma:
    print $a + $b . "<br>";

    // Subitração:
    print $a - $b . "<br>";

    // Multiplicação:
    print $a * $b . "<br>";

    // Resto da Divisão:
    print $a % $b . "<br>";

    // Divisão inteira: 
    print $a / $b . "<br>";

    // Esponetisação(potencia)
    print $a ** $b . "<br>";
?>